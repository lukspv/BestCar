    private void jbBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBuscarActionPerformed
        jlaviso.setText("");
        int id;

        //Zona zona = new Zona();
        if ("".equals(jtcodigo.getText()) || jtcodigo.getText() == null) {
            jtnome.setText("");
            jlaviso.setForeground(Color.red);
            jlaviso.setText("*Por favor preencha o campo codigo");
            jtcodigo.requestFocus();

        } else {
            ZonaDao zdao = new ZonaDao();
            id = (Integer.parseInt(jtcodigo.getText()));
            try {
                Zona zona = zdao.Buscar(id);
                if (zona.getZoid() != id || jtcodigo.getText() == null) {
                    System.out.println("*Zona n達o cadastrada");
                    jlaviso.setForeground(Color.red);
                    jlaviso.setText("*Zona n達o Cadastrada");
                } else {
                    //jtCodigo.setText(zona.getZoid());
                    jtnome.setText(zona.getZonome());

                }

            } catch (SQLException ex) {
                System.out.println("*Erro ao Buscar " + ex.getMessage());
                jlaviso.setForeground(Color.red);
                jlaviso.setText("*Erro ao Buscar " + ex.getMessage());
            }

        }


    }//GEN-LAST:event_jbBuscarActionPerformed

